
package loop;

public class LoopEx2 {
    
    public static void main(String[] args) {
        
        System.out.println("Printing on the screen numbers from 10 to 20: ");
        int n1 = 10;
        while (n1 <= 20) {            
            System.out.println(n1);
            n1++;
        }
    }
    
    
}
